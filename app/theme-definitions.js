export const DEFAULT_THEME_ID = "notes";

export function getThemeVars(themeId) {
  return T[themeId] || T[DEFAULT_THEME_ID];
}

export function applyThemeToDocument(themeId) {
  if (typeof document === "undefined") return;
  const vars = getThemeVars(themeId);
  const root = document.documentElement;
  for (const [key, value] of Object.entries(vars)) {
    root.style.setProperty(key, value);
  }
}

/** Mini preview chips for the settings grid */
export function getThemePreview(themeId) {
  const v = getThemeVars(themeId);
  return {
    bg: v["--note-page-bg"],
    sidebar: v["--note-sidebar-bg"],
    heading: v["--note-sidebar-pill-active-fg"],
    accent: v["--note-accent-secondary"] || v["--note-accent"],
  };
}
